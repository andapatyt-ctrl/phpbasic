<h3>Try Catch</h3>

<?php

try {
    $x = 10 / 'Hello';  
} catch (DivisionByZeroError $e) {  //จับข้อผิดพลาดการหารด้วยศูนย์
    echo 'เกิดข้อผิดพลาด ไม่สามารถหารด้วยศูนย์ได้';
} catch (TypeError $e) {  //จับข้อผิดพลาดการใช้ชนิดข้อมูลผิด
    echo 'เกิดข้อผิดพลาด ไม่สามารถหารด้วยตัวอักษรได้';
} catch (Throwable $e) {  //จับข้อผิดพลาดทั่วไป
    echo 'เกิดข้อผิดพลาด ไม่สามารถดำเนินการได้';
}
?>